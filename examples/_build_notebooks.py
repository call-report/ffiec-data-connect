        "",
        "for period in target_periods:",
        "    print(f'Fetching {period}...', end=' ')",
        "    df = collect_data(",
        "        creds,",
        "        reporting_period=period,",
        "        rssd_id=RSSD_ID,",
        "        series='call',",
        "        output_type='pandas',",
