        "NUM_QUARTERS = 8",
        "",
        "all_periods = collect_reporting_periods(creds, series='call')",
        "# Skip the latest period — it may be a future quarter not yet filed",
        "target_periods = all_periods[-(NUM_QUARTERS + 1):-1]",
